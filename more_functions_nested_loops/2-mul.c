#include "main.h"
#include <stdio.h>

/**
*mul - multiplie deux entiers
*@a: premier entier
*@b: deuxième entier
*Description:
*Cette fonction prend deux entiers en paramètres et renvoie
*le résultat de leur multiplication.
*Return: le produit de a et b
*/
int mul(int a, int b)
{
	return (a * b);
}
